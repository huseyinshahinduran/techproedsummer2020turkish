package day14dowhileloop;

public class DoWhile01 {

	public static void main(String[] args) {
		
		int num1 = 1;
		
		do {
			
			System.out.print(num1+" ");
			num1++;
			
		}while(num1<4);
		
		System.out.println();
		System.out.println("-------------");
		
		
//10 ile 20 arasindaki tek tamsayilari ekrana do-while loop ile yazdirin.
		
		int num2 = 11;
		
		do {
			
			System.out.print(num2+" ");
			num2+=2;
			
		}while(num2<20);
		
	}

}
