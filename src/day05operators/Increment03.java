package day05operators;

public class Increment03 {

	public static void main(String[] args) {
		
		// int data typ'inda iki tane variable olusturun.
	   // Bu varibale'lari en kis yolu kullanarak 1 artirin.
	  //En kisa yol sadece 1 artirmak icin kullanilir.
		
		int sayi1 = 12;
		sayi1++; // variabe isminden sonra iki kere + sembolu yazarsaniz
		        //o variable'in degerini 1 artirmis olursunuz.
		System.out.println(sayi1); //13
	
//////////////////////////////////////////////////////////////////////////////////////	
		
		int sayi2 = 19;
		sayi2++;
		System.out.println(sayi2); //20
		
		
		

	}

}
